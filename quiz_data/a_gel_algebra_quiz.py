def build_questions(raw_questions):
    """
    Μετατρέπει tuples της μορφής:
    (question, choices, answer, difficulty)
    σε λίστα από dicts.

    NOTE:
    Αν θες αργότερα να βάλεις και explanation/λύση,
    μπορείς να προσθέσεις νέο πεδίο εδώ.
    """
    questions = []
    for q, c, a, d in raw_questions:
        questions.append({
            "question": q,
            "choices": c,
            "answer": a,
            "difficulty": d
        })
    return questions


